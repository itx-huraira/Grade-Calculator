let englishMarks = 60;
let urduMarks = 60;
let average = (englishMarks + urduMarks)/2;

if (average < 60){
    console.log("Grade is F")
}
else if(average<70){
    console.log("Grade is D")
}
else if(average<80){
    console.log("Grade is c")
}
else if(average<90){
    console.log("Grade is B")
}
else if(average<100){
    console.log("Grade is A")
}